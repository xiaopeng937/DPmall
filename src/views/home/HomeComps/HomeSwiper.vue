<template>
	<swiper>
			<swiper-item v-for="(item,index) in banners" :key="index">
				<a :href="item.link">
					<img :src="item.image" alt=""  @load="swiperLoad"/>
				</a>
			</swiper-item>
	</swiper>
</template>

<script>
	import {Swiper, SwiperItem} from 'components/common/swiper'
	export default {
    name: 'HomeSwiper',
    data() {
      return {
        isload: false
      }
    },
    components: {
      Swiper,
      SwiperItem
    },
    props: {
      banners: {
        type: Array,
        default() {
          return []
        }
      }
    },
		methods: {
      swiperLoad() {
        if(!this.isload) {
          this.$emit("swiperTop")
          this.isload = true
        }
      }   
    }
	}
</script>

